/* What is difference between Interface and Abstract Class ?
Answer :
    Interface ::
        1. Interface support multiple inheritance.
        2. Interface doesn't contains data member.
        3. Interface doesn't contains constructors.
        4. An interface contains only incomplete member (signature of member).
        5. An interface can't have access modifiers by default everything is assumed as public.
        6. Member of interface can't be static.

    Abstract ::
        1. Abstract class doesn't support multiple inheritance.
        2. Abstract class contains data member.
        3. An Abstract class contains both incomplete (abstract) and complete member.
        4. An abstract class can contain access modifiers for the subs, functions, properties.
        5. Only complete member of abstract class can be static.

