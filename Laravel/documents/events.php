Question: Explain Events in laravel ?
Answer :
    An event is an action or occurrence recognized by a program that may be handled by the program or code. Laravel events
    provides a simple observer implementation, that allowing you to subscribe and listen for various events/actions that occur
    in your application.

    All Event classes are generally stored in the app/Events directory, while their listeners are stored
    in app/Listeners of your application.