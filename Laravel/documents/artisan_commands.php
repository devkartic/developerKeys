Question: What is PHP artisan. List out some artisan commands ?
Answer :
    PHP artisan is the command line interface/tool included with Laravel. It provides a number of helpful commands that
    can help you while you build your application easily. Here are the list of some artisan command:-

    a. php artisan list
    b. php artisan help
    c. php artisan tinker
    d. php artisan make
    e. php artisan –version
    f. php artisan make model model_name
    g. php artisan make controller controller_name