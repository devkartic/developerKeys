Question. What are pros and cons of using Laravel Framework?
Answer :
    Pros of using Laravel Framework
        a. Laravel framework has in-built lightweight blade template engine to speed up compiling task and create
        layouts with dynamic content easily.
        b. Hassles code reusability.
        c. Eloquent ORM with PHP active record implementation
        d. Built in command line tool “Artisan” for creating a code skeleton , database structure and build their migration

    Cons of using laravel Framework
        a. Development process requires you to work with standards and should have real understanding of programming
        b. Laravel is new framework and composer is not so strong in compare to npm (for node.js), ruby gems and python pip.
        c. Development in laravel is not so fast in compare to ruby on rails.
        d. Laravel is lightweight so it has less inbuilt support in compare to django and rails. But this problem can be solved by
        integrating third party tools, but for large and very custom websites it may be a tedious task


