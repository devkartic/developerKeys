/* Install nodemon

Either through cloning with git or by using npm (the recommended way):

$ sudo npm install -g nodemon
And nodemon will be installed globally to your system path.

You can also install nodemon as a development dependency:

npm install --save-dev nodemon
With a local installation.txt, nodemon will not be available in your system path.
Instead, the local installation.txt of nodemon can be run by calling it from within an
npm script (such as npm start) or using npx nodemon.

*/
